package Humeyra.ders13;

public class NestedForLoopsYildizli3 {
    public static void main(String[] args) {
        /*
        * * * * *
        * * * *
        * * *
        * *
        *
         */
        int input=7;
        for (int i = input; i>=1; i--) {
            for (int j = i; j >=1 ; j--) {
                System.out.print("* ");

            }
            System.out.println("");

        }
    }
}
